# ---------------------------------------------------------------------------
# Data classes
# ---------------------------------------------------------------------------

@dataclass
class Segment:
    name: str
    description: str       # persona description for system prompt
    weight: float          # population weight (0-1, all segments sum to 1)
    rationale: str         # why this segment is relevant

@dataclass
class SurveyQuestion:
    id: str
    text: str
    type: str              # "likert5" | "binary" | "wtp" | "open" | "multi_select"
    scale_label: str = ""  # e.g. "1=Strongly Disagree, 5=Strongly Agree"
    options: list = field(default_factory=list)   # for multi_select
    condition: str = "neutral"                    # "neutral" | "anonymous" | "named"
    use_ssr: bool = False  # if True, elicit free-text + embed → Likert pmf (Maier et al. 2025)
    anchors: list = field(default_factory=list)   # 5 anchor statements for SSR (auto-generated by Agent 1)

@dataclass
class ClarifyQuestion:
    id: str               # stable key — GUI uses for state
    axis: str             # "geography"|"decision"|"segments"|"sensitivity"|"success_metric"|"constraints"
    text: str             # question text shown to user
    why: str              # one sentence: what this answer changes downstream (GUI tooltip)
    answer_type: str      # "text" | "single_select" | "multi_select" | "number"
    options: list         # suggested answers — GUI renders as radio/chip/checkbox
    allow_freeform: bool  # if True, GUI adds "Other..." free-text box
    skippable: bool       # if True, GUI shows "Let agent decide" button
    priority: str         # "must" | "nice" — GUI hides "nice" behind expander

@dataclass
class SimulatedResponse:
    segment_name: str
    persona_detail: str
    question_id: str
    raw_response: str
    parsed_value: Any           # numeric for likert/wtp/binary, str for open; float ev for SSR
    pmf: list | None = None     # 5-element Likert pmf from SSR; None for DLR/other types

@dataclass
class SegmentResult:
    segment: Segment
    question_summaries: dict[str, Any]   # question_id -> aggregated stats
    open_themes: list[str]

@dataclass
class AnalysisOutput:
    segment_results: list[SegmentResult]
    overall_summary: dict[str, Any]
    recommendations: list[str]
    risk_flags: list[str]
    target_segment: str


