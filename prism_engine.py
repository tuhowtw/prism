_GENERIC_ANCHORS_AGREEMENT = [
    "I strongly disagree with this.",
    "I somewhat disagree with this.",
    "I neither agree nor disagree.",
    "I somewhat agree with this.",
    "I strongly agree with this.",
]
_GENERIC_ANCHORS_FREQUENCY = [
    "I never do this.",
    "I rarely do this.",
    "I sometimes do this.",
    "I often do this.",
    "I very frequently or always do this.",
]
_GENERIC_ANCHORS_LIKELIHOOD = [
    "This is very unlikely to happen for me.",
    "This is somewhat unlikely for me.",
    "I'm unsure or indifferent about this.",
    "This is somewhat likely for me.",
    "This is very likely to happen for me.",
]

# Module-level cache: anchor tuple → list of embedding vectors (populated once per run)
_ANCHOR_EMBED_CACHE: dict[tuple[str, ...], list[list[float]]] = {}


def _resolve_anchors(q: SurveyQuestion) -> list[str]:
    """Return q.anchors if Agent 1 provided 5; else pick generic family from scale_label."""
    if q.anchors and len(q.anchors) == 5:
        return q.anchors
    label = q.scale_label.lower()
    if any(w in label for w in ("often", "never", "frequent", "rare")):
        return _GENERIC_ANCHORS_FREQUENCY
    if any(w in label for w in ("likely", "unlikely", "intent", "purchase")):
        return _GENERIC_ANCHORS_LIKELIHOOD
    return _GENERIC_ANCHORS_AGREEMENT


def _cosine(a: list[float], b: list[float]) -> float:
    dot = sum(x * y for x, y in zip(a, b))
    norm_a = sum(x * x for x in a) ** 0.5
    norm_b = sum(x * x for x in b) ** 0.5
    if norm_a == 0.0 or norm_b == 0.0:
        return 0.0
    return dot / (norm_a * norm_b)


async def _aembed(text: str) -> list[float]:
    """Embed a single string via litellm (uses OPENAI_API_KEY from env)."""
    resp = await litellm.aembedding(model=EMBED_MODEL, input=[text])
    return resp.data[0]["embedding"]


async def _ssr_score(response_text: str, anchors: list[str]) -> tuple[list[float], float]:
    """Map a free-text response to a Likert pmf via cosine similarity to anchors.

    Returns (pmf, expected_value) where pmf is length-5 and ev is in [1, 5].
    Uses ε=0, T=1 defaults from Maier et al. 2025.
    """
    key = tuple(anchors)
    if key not in _ANCHOR_EMBED_CACHE:
        _ANCHOR_EMBED_CACHE[key] = [await _aembed(a) for a in anchors]
    anchor_vecs = _ANCHOR_EMBED_CACHE[key]
    resp_vec = await _aembed(response_text)
    sims = [_cosine(resp_vec, av) for av in anchor_vecs]
    min_sim = min(sims)
    shifted = [max(0.0, s - min_sim) for s in sims]
    total = sum(shifted) or 1.0
    pmf = [s / total for s in shifted]
    ev = sum((i + 1) * p for i, p in enumerate(pmf))
    return pmf, ev


async def _prewarm_anchor_cache(questions: list[SurveyQuestion]) -> None:
    """Pre-embed anchors for all SSR questions before simulation fan-out."""
    seen: set[tuple[str, ...]] = set()
    for q in questions:
        if q.type == "likert5" and q.use_ssr:
            anchors = _resolve_anchors(q)
            key = tuple(anchors)
            if key not in seen and key not in _ANCHOR_EMBED_CACHE:
                seen.add(key)
                _ANCHOR_EMBED_CACHE[key] = [await _aembed(a) for a in anchors]


