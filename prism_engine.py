def _build_question_instruction(q: SurveyQuestion) -> str:
    prefix = _CONDITION_PREFIX.get(q.condition, "")
    if q.type == "likert5":
        if q.use_ssr:
            body = (
                f"{q.text}\n"
                f"In 1-2 sentences, describe honestly and specifically how you feel about this. "
                f"Do not mention numbers or rating scales."
            )
        else:
            body = (
                f"{q.text}\n"
                f"Rate on a scale of 1 to 5. {q.scale_label}. "
                f"Reply with a single integer (1, 2, 3, 4, or 5) and nothing else."
            )
    elif q.type == "binary":
        body = (
            f"{q.text}\n"
            f"{q.scale_label}. Reply with exactly 'yes' or 'no' and nothing else."
        )
    elif q.type == "wtp":
        body = (
            f"{q.text}\n"
            f"{q.scale_label}. Reply with a number only (no currency symbol, no text). "
            f"If you would not pay anything, reply with 0."
        )
    elif q.type == "multi_select":
        letters = [chr(ord("A") + i) for i in range(len(q.options))]
        option_lines = "\n".join(f"  {l}: {o}" for l, o in zip(letters, q.options))
        body = (
            f"{q.text}\n"
            f"Options:\n{option_lines}\n"
            f"Reply with the letters of ALL options that apply, comma-separated (e.g. A,C,E). "
            f"Reply with letters only, no other text."
        )
    else:
        body = f"{q.text}\nAnswer in 1-2 sentences. Be honest and specific."
    return prefix + body


def _parse_response(q: SurveyQuestion, raw: str) -> Any:
    text = raw.strip().lower()
    if q.type == "likert5":
        if q.use_ssr:
            return raw.strip()  # SSR: return free text; _simulate_one replaces with float ev
        match = re.search(r"[1-5]", text)
        return int(match.group()) if match else None
    elif q.type == "binary":
        if "yes" in text:
            return 1
        elif "no" in text:
            return 0
        return None
    elif q.type == "wtp":
        match = re.search(r"\d[\d,]*", text)
        return float(match.group().replace(",", "")) if match else None
    elif q.type == "multi_select":
        letters = [chr(ord("A") + i) for i in range(len(q.options))]
        selected = []
        for letter in re.findall(r"[A-Za-z]", raw):
            ul = letter.upper()
            if ul in letters:
                idx = ord(ul) - ord("A")
                if idx < len(q.options):
                    selected.append(q.options[idx])
        return selected if selected else None
    else:
        return raw.strip()


async def _simulate_one(
    segment: Segment,
    question: SurveyQuestion,
    semaphore: asyncio.Semaphore,
    api_key: str | None = None,
    max_retries: int = 10,
) -> SimulatedResponse:
    instruction = _build_question_instruction(question)
    delay = 20.0
    for attempt in range(max_retries):
        try:
            async with semaphore:
                raw = await _achat(
                    model=SIM_MODEL,
                    system=segment.description,
                    user=instruction,
                    max_tokens=256,
                    temperature=1.0,
                    api_key=api_key,
                )
            break
        except litellm.exceptions.RateLimitError:
            if attempt == max_retries - 1:
                raise
            await asyncio.sleep(delay)
            delay = min(delay * 1.5, 120.0)
    if question.type == "likert5" and question.use_ssr:
        anchors = _resolve_anchors(question)
        pmf, ev = await _ssr_score(raw, anchors)
        return SimulatedResponse(
            segment_name=segment.name,
            persona_detail=segment.description[:80] + "...",
            question_id=question.id,
            raw_response=raw,
            parsed_value=ev,
            pmf=pmf,
        )
    return SimulatedResponse(
        segment_name=segment.name,
        persona_detail=segment.description[:80] + "...",
        question_id=question.id,
        raw_response=raw,
        parsed_value=_parse_response(question, raw),
        pmf=None,
    )


async def run_simulation_async(
    segments: list[Segment],
    questions: list[SurveyQuestion],
    responses_per_cell: int = 10,
    max_concurrent: int = 3,
    api_key: str | None = None,
    progress_callback=None,
) -> list[SimulatedResponse]:
    semaphore = asyncio.Semaphore(max_concurrent)

    # Pre-embed anchors for SSR questions (once per question, reused across all respondents)
    await _prewarm_anchor_cache(questions)

    total = len(segments) * len(questions) * responses_per_cell
    completed = 0

    async def _sim_with_progress(segment, question):
        nonlocal completed
        result = await _simulate_one(segment, question, semaphore, api_key=api_key)
        completed += 1
        if progress_callback is not None:
            progress_callback(completed, total)
        return result

    tasks = [
        _sim_with_progress(segment, question)
        for segment in segments
        for question in questions
        for _ in range(responses_per_cell)
    ]
    results = await asyncio.gather(*tasks, return_exceptions=True)
    return [r for r in results if isinstance(r, SimulatedResponse)]


def run_simulation(
    segments: list[Segment],
    questions: list[SurveyQuestion],
    responses_per_cell: int = 10,
    max_concurrent: int = 3,
    api_key: str | None = None,
    progress_callback=None,
) -> list[SimulatedResponse]:
    return asyncio.run(
        run_simulation_async(
            segments, questions, responses_per_cell, max_concurrent,
            api_key=api_key, progress_callback=progress_callback,
        )
    )


